export function getTemplate(id: string): LegacyTemplate | undefined {
  return LEGACY_TEMPLATES.find((t) => t.id === id);
}

export function listTemplates(): Array<{ id: string; name: string; documentType: string; category: string; troSensitive: boolean; emergencyEligible: boolean }> {
  return LEGACY_TEMPLATES.map(({ id, name, documentType, category, troSensitive, emergencyEligible }) => ({
    id, name, documentType, category, troSensitive, emergencyEligible,
  }));
}

