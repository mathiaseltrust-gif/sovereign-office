function sig(vars: Record<string, string>, role = "Chief Justice & Trustee"): string {
  return [
    `SOVEREIGN OFFICE OF THE CHIEF JUSTICE & TRUSTEE`,
    ``,
    `_________________________________      Date: ________________`,
    `${vars.officerName ?? role}`,
    `${vars.officerTitle ?? role}`,
    `Sovereign Office of the Chief Justice & Trustee`,
    ``,
    `_________________________________      Date: ________________`,
    `Intake Officer / Authorized Representative`,
    `Badge/ID: _______________________`,
  ].join("\n");
}

